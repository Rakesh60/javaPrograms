import java.text.SimpleDateFormat;
import java.util.Date;
import java.util.Scanner;

public class InputScanner {
    public static void main(String[] args) {
        @SuppressWarnings("resource")
        //*Scanner class  */
        Scanner scan =new Scanner(System.in);
        System.out.println("Enter the value of var1");
        //taking intergr as input
        int var1=scan.nextInt();
        System.out.println("Enter the value of var2");
        int var2=scan.nextInt();
        System.out.println("The sum of "+var1+" and "+var2+" = "+(var1+var2));
        //taking float as input
        System.out.println("Enter a float value");
        float str1=scan.nextFloat();
        System.out.println(str1);
        //taking string as input
        //?it only takes takes the first occuring string not (Rakesh kumar)
        //? it only take (rakesh)
        System.out.println("Enter your  name");
        String name=scan.next();
        System.out.println("My name is :"+name);

        //~ So to tackle this problem we use scan.nextLine()
        System.out.println("Enter your Full name");

        scan.nextLine();
        String fullName=scan.nextLine();
        System.out.println("My name is :"+fullName);

        //? now taking character as input
        System.out.println("Enter a character");
        char ch =scan.next().charAt(0);
        System.out.println("Character is: "+ch);

        //* Printing Date in java */ 
        SimpleDateFormat ft=new SimpleDateFormat("dd-mm-yyyy");
        String todayDate=ft.format(new Date());
        System.out.println(todayDate);

    }
}
